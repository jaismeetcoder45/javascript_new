//Switch Case
const month = "March"
switch (month) {
    case "jan":
        console.log("January");
        break;
    case "Feb":
        console.log("feb");
        break;
    case "March":
        console.log("march");
        break;
    case "April":
        console.log("april");
        break;
    default:
        console.log("Deafult case matched");
        break;
}